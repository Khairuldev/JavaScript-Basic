//membuat function dengan parameter

function sayHelloWorld(firstname, middlename ,lastname){
    document.writeln(`<p><strong>Hello ${firstname} ${middlename} ${lastname} </strong></p>`)
}

//memanggil function DENGAN PARAMETER

sayHelloWorld("khairul", "Anwar", "Tanjung");   
sayHelloWorld("Muhammad", "Khoir", "BB"); 
sayHelloWorld(1 ," ",2); 
